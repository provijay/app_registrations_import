#!/usr/bin/env bash
set -euo pipefail

# Requires: Azure CLI (`az`), jq, and Terraform.
# Login before running: az login --tenant <TENANT_ID>
# Usage: bash import.sh

tf() { terraform "$@"; }

# Resolve application objectId from appId (clientId)
app_oid_from_appid() {
  local appid="$1"
  az ad app show --id "$appid" --query id -o tsv
}

# Resolve service principal objectId from appId (clientId)
sp_oid_from_appid() {
  local appid="$1"
  az ad sp show --id "$appid" --query id -o tsv
}

# Resolve objectId by display name (may be ambiguous!)
app_oid_from_name() {
  local name="$1"
  az ad app list --display-name "$name" --query "[0].id" -o tsv
}
sp_oid_from_name() {
  local name="$1"
  az ad sp list --display-name "$name" --query "[0].id" -o tsv
}

import_application() {
  local res="$1" appid="$2"
  local oid
  oid="$(app_oid_from_appid "$appid")"
  echo "Importing azuread_application.${res} (/applications/${oid})"
  tf import "azuread_application.${res}" "/applications/${oid}"
}
import_service_principal() {
  local res="$1" appid="$2"
  local oid
  oid="$(sp_oid_from_appid "$appid")"
  echo "Importing azuread_service_principal.${res} (/servicePrincipals/${oid})"
  tf import "azuread_service_principal.${res}" "/servicePrincipals/${oid}"
}

import_application_oid() {
  local res="$1" oid="$2"
  echo "Importing azuread_application.${res} (/applications/${oid})"
  tf import "azuread_application.${res}" "/applications/${oid}"
}
import_service_principal_oid() {
  local res="$1" oid="$2"
  echo "Importing azuread_service_principal.${res} (/servicePrincipals/${oid})"
  tf import "azuread_service_principal.${res}" "/servicePrincipals/${oid}"
}

import_application_name() {
  local res="$1" name="$2"
  local oid
  oid="$(app_oid_from_name "$name")"
  echo "Importing azuread_application.${res} (/applications/${oid})"
  tf import "azuread_application.${res}" "/applications/${oid}"
}
import_service_principal_name() {
  local res="$1" name="$2"
  local oid
  oid="$(sp_oid_from_name "$name")"
  echo "Importing azuread_service_principal.${res} (/servicePrincipals/${oid})"
  tf import "azuread_service_principal.${res}" "/servicePrincipals/${oid}"
}

